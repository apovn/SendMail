    private void sendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendBtnActionPerformed
        // TODO add your handling code here:
        
        mTitle = titleTf.getText();
        mContent = mailContent.getText();
        
        if ( mTitle.trim().equals("") || mContent.trim().equals("") ){
            JOptionPane.showMessageDialog(null, "Fill title and content");
            return;
        }
        
        sendBtn.setText("Sending...");
        jProgressBar1.setIndeterminate(true);
        
        JavaEmail.countNumberPersonSentToday = 0;
        statusContent.setText("");
        
        if ( rdHtml.isSelected() ){
            isTextFormat = false;
        }
        
        javaEmail = new JavaEmail();
        javaEmail.setMailServerProperties();
        timer.schedule(new RemindTask1(), 0, timePeriod);
    }//GEN-LAST:event_sendBtnActionPerformed

