    class RemindTask1 extends TimerTask {

        @Override
        public void run() {
            
            if (JavaEmail.totalMemSent >= JavaEmail.recipientArray.length) {	// da gui het danh sach
                statusContent.setText("Sending all email finished!");
                JOptionPane.showMessageDialog(null, "Sending all email finished!");
                
                sendBtn.setText("Send");
                jProgressBar1.setValue(100);
                jProgressBar1.setIndeterminate(false);    
                
                timer.cancel();
                return;
            }

            // setup and send mail
            try {
                javaEmail.sendEmail();
            } catch (AuthenticationFailedException ex){
                statusContent.setText("Sai username, password hoặc chưa thiết lập bảo mật.");
                // https://www.google.com/settings/security/lesssecureapps
                return;
            } catch (MessagingException ex) {
                MyLogger.logger.log(Level.WARNING, "Lỗi chưa thiết lập bảo mật.");
            } catch (Exception ex) {
                MyLogger.logger.log(Level.WARNING, "Error2 không xác định");
            }
        }
    }
    
