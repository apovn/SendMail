    private void btnChooseFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChooseFileActionPerformed
        final JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(this);
        StringBuilder text = new StringBuilder();
        
        try {
            // Open an input stream
            Scanner reader = new Scanner(fc.getSelectedFile());
            filePath.setText( fc.getSelectedFile().getAbsolutePath() );
            while (reader.hasNextLine()) {
                String line = reader.nextLine();
                text.append(line);
            }
            
            if ( text.toString().contains(",") ){
                recipientBrowseArray = text.toString().split(",");
            } else {
                recipientBrowseArray = text.toString().split(";");
            }
            
            isBrowseFile = true;
            totalNickLbl.setText( "("  + recipientBrowseArray.length + " nicks)" );
            MyLogger.logger.log(Level.INFO, "Total member in browse file: {0}", recipientBrowseArray.length);
        } catch( Exception ex ) {
            MyLogger.logger.log(Level.WARNING, "Error3: {0}", ex);
        }
    }//GEN-LAST:event_btnChooseFileActionPerformed

    private void initValue() {
        
        new MyLogger();
        
        // init GUI
        rdText.setSelected(true);
        ButtonGroup group = new ButtonGroup();
        group.add(rdText);
        group.add(rdHtml);
        
        // load config
        prop = new Properties();
        try {        
            prop.load(new FileInputStream("config.txt"));
            username = prop.getProperty("gmailuser").trim();
            password = prop.getProperty("gmailpass").trim();
            String tmp = prop.getProperty("totalPersonPerDay").trim();
            String tmp2 = prop.getProperty("timePerSendMailMin").trim();
            timePeriod = Integer.parseInt(tmp2) * 1000 * 60; // => second => milisecond
            
            if (tmp != null) {
                int num = Integer.parseInt(tmp);
                if (num < totalPersonSentPmPerDayLimit) {
                    totalPersonSentPmPerDayLimit = num;
                }
            }
            
        } catch (IOException ex) {
            MyLogger.logger.log(Level.WARNING, "Can not read config or file is not exist");
        } catch (Exception ex) {
            MyLogger.logger.log(Level.WARNING, "Error1: {0}", ex);
        }
        
    }
    
